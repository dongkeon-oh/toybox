package com.dongkeonoh.toybox.vo;

public class CommonVo {
	private String cgr_group;
	private String cgr_group_name;
    private String cgr_note;
    private String cgr_useyn;
    private String cnt;
    
	public String getCgr_group() {
		return cgr_group;
	}
	public void setCgr_group(String cgr_group) {
		this.cgr_group = cgr_group;
	}
	public String getCgr_group_name() {
		return cgr_group_name;
	}
	public void setCgr_group_name(String cgr_group_name) {
		this.cgr_group_name = cgr_group_name;
	}
	public String getCgr_note() {
		return cgr_note;
	}
	public void setCgr_note(String cgr_note) {
		this.cgr_note = cgr_note;
	}
	public String getCgr_useyn() {
		return cgr_useyn;
	}
	public void setCgr_useyn(String cgr_useyn) {
		this.cgr_useyn = cgr_useyn;
	}
	public String getCnt() {
		return cnt;
	}
	public void setCnt(String cnt) {
		this.cnt = cnt;
	}
}
